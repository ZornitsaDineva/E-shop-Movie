using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using E_shop_Movie.Data;
using E_shop_Movie.Models;

namespace E_shop_Movie.Pages.Movies
{
    public class DetailsModel : PageModel
    {
        private readonly E_shop_Movie.Data.E_shop_MovieContext _context;

        public DetailsModel(E_shop_Movie.Data.E_shop_MovieContext context)
        {
            _context = context;
        }

        public Movie Movie { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Movie = await _context.Movie.FirstOrDefaultAsync(m => m.ID == id);

            if (Movie == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
